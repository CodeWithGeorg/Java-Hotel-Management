package src;

public class Customer {
    public String id;
    public String name;
    public String phone;
    public String roomNumber;

    // Constructor to initialize customer details
    public Customer(String id, String name, String phone, String roomNumber) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.roomNumber = roomNumber;
    }

}
